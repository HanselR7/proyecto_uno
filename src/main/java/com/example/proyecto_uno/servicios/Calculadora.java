package com.example.proyecto_uno.servicios;

public class Calculadora {
    public int suma (int numero_1, int numero_2) {
        return numero_1 + numero_2;
    }

    public int resta (int numero_1, int numero_2) {
        return numero_1 - numero_2;
    }

    public int multiplicacion (int numero_1, int numero_2) {
        return numero_1 * numero_2;
    }

    public double division (double numero_1, double numero_2) {
        return numero_1 / numero_2;
    }
}
